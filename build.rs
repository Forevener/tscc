fn format_f32(v: f32) -> String {
    if v.is_nan() {
        "f32::NAN".to_string()
    } else if v.is_infinite() {
        if v.is_sign_positive() {
            "f32::INFINITY".to_string()
        } else {
            "f32::NEG_INFINITY".to_string()
        }
    } else {
        // Emit as the bit pattern to preserve -0.0 and avoid lossy Debug formatting.
        format!("f32::from_bits({})", v.to_bits())
    }
}

fn format_f64(v: f64) -> String {
    if v.is_nan() {
        "f64::NAN".to_string()
    } else if v.is_infinite() {
        if v.is_sign_positive() {
            "f64::INFINITY".to_string()
        } else {
            "f64::NEG_INFINITY".to_string()
        }
    } else {
        format!("f64::from_bits({})", v.to_bits())
    }
}

fn val_type_to_rust(v: ValType) -> String {
    match v {
        ValType::I32 => "ValType::I32".to_string(),
        ValType::I64 => "ValType::I64".to_string(),
        ValType::F32 => "ValType::F32".to_string(),
        ValType::F64 => "ValType::F64".to_string(),
        ValType::V128 => "ValType::V128".to_string(),
        ValType::Ref(rt) => panic!("unexpected ref type {rt:?} in helpers wasm"),
    }
}
